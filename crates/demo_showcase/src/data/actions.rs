/// A user-facing notification (toast).
#[derive(Debug, Clone)]
pub struct Notification {
    /// Notification severity/style.
    pub severity: NotificationSeverity,
    /// Notification title.
    pub title: String,
    /// Optional detailed message.
    pub message: Option<String>,
    /// How long to show (None = sticky/until dismissed).
    pub duration_ms: Option<u64>,
}

/// Severity/style of a notification.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum NotificationSeverity {
    /// Informational notice.
    Info,
    /// Success message.
    Success,
    /// Warning message.
    Warning,
    /// Error message.
    Error,
}

impl Notification {
    /// Create an info notification.
    #[must_use]
    pub fn info(title: impl Into<String>) -> Self {
        Self {
            severity: NotificationSeverity::Info,
            title: title.into(),
            message: None,
            duration_ms: Some(3000),
        }
    }

    /// Create a success notification.
    #[must_use]
    pub fn success(title: impl Into<String>) -> Self {
        Self {
            severity: NotificationSeverity::Success,
            title: title.into(),
            message: None,
            duration_ms: Some(3000),
        }
    }

    /// Create a warning notification.
    #[must_use]
    pub fn warning(title: impl Into<String>) -> Self {
        Self {
            severity: NotificationSeverity::Warning,
            title: title.into(),
            message: None,
            duration_ms: Some(5000),
        }
    }

    /// Create an error notification.
    #[must_use]
    pub fn error(title: impl Into<String>) -> Self {
        Self {
            severity: NotificationSeverity::Error,
            title: title.into(),
            message: None,
            duration_ms: None, // Errors are sticky
        }
    }

    /// Add a detailed message.
    #[must_use]
    pub fn with_message(mut self, message: impl Into<String>) -> Self {
        self.message = Some(message.into());
        self
    }

    /// Set custom duration.
    #[must_use]
    pub const fn with_duration(mut self, ms: u64) -> Self {
        self.duration_ms = Some(ms);
        self
    }

    /// Make the notification sticky (no auto-dismiss).
    #[must_use]
    pub const fn sticky(mut self) -> Self {
        self.duration_ms = None;
        self
    }
}

