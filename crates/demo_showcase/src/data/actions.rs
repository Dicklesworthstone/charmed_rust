/// Messages generated by actions for UI updates.
#[derive(Debug, Clone)]
pub enum ActionMsg {
    /// A job was created.
    JobCreated(Id),
    /// A job was started.
    JobStarted(Id),
    /// A job was cancelled.
    JobCancelled(Id),
    /// A job was retried.
    JobRetried(Id),
    /// A job completed.
    JobCompleted(Id),
    /// A job failed.
    JobFailed(Id, String),
    /// A deployment was started.
    DeploymentStarted(Id),
    /// A deployment succeeded.
    DeploymentSucceeded(Id),
    /// A deployment failed.
    DeploymentFailed(Id),
    /// A deployment was rolled back.
    DeploymentRolledBack(Id),
    /// A notification should be shown.
    ShowNotification(Notification),
    /// A notification should be dismissed.
    DismissNotification,
}

impl ActionMsg {
    /// Convert to a bubbletea Message.
    #[must_use]
    pub fn into_message(self) -> Message {
        Message::new(self)
    }
}

